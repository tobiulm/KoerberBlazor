namespace BlazingPizza.ComponentsLibrary.Map
{
    /// <summary>
    /// Repräsentiert einen Punkt auf der Karte mit X- und Y-Koordinaten.
    /// </summary>
    public class Point
    {
        /// <summary>
        /// Ruft die X-Koordinate des Punktes ab oder legt sie fest.
        /// </summary>
        public double X { get; set; }

        /// <summary>
        /// Ruft die Y-Koordinate des Punktes ab oder legt sie fest.
        /// </summary>
        public double Y { get; set; }
    }
}